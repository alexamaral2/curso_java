package secao5;

public class Switch {

	public static void main(String[] args) {

		// 11 - switch case e break

		// para validar dida de semana baseado no número
		// 1 = Domingo
		// 7 = Sábado

		int diaDaSemana = 1;

		switch (diaDaSemana) {
		case 1:
			System.out.println("Domingo");
			break;
		case 2:
			System.out.println("Segunda-feira");
			break;
		case 3:
			System.out.println("Terça-feira");
			break;
		}

	}

}
